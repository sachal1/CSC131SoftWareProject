class Movie:

    def __int__(self, title, year, directors, genre, actors, language, awards, poster, imdb_id):
        self.title = title
        self.year = year
        self.directors = directors
        self.genre = genre
        self.actors = actors
        self.language = language
        self.awards = awards
        self.poster = poster
        self.imdb_id = imdb_id
